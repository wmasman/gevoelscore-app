    d = build_daily_frame(df, wt, vo)
    print(f"\n   Fit sample: {len(d)} days with RHR present.")
    print(f"   Weight interpolated to daily. Largest weigh-in gap: "
          f"{WEIGH_GAP_DAYS} days ({WEIGH_GAP_FROM} to {WEIGH_GAP_TO}), which "
          f"sits directly under the post-2023 leverage window; the 2026 anchor "
          f"({ANCHOR_2026_KG} kg) is participant-reported, not in the dump.")
    print(f"   Citalopram plasma nonzero from "
          f"{d.loc[d['dose']>0,'date'].min().date()}.")

    results = {}

    # (i) literature curve, tau sensitivity grid
    for tau_m in TAU_GRID_MONTHS:
        tau_y = tau_m / 12.0
        print(f"\n   --- LITERATURE decond curve, tau = {tau_m:.0f} months ---")
        betas, resid, beta, X, names = block_bootstrap_betas(
            d, None, tau_years=tau_y, garmin=False)
        for nm in names:
            pt, lo, hi = betas[nm]
            print(f"     {nm:12s}: {pt:8.3f}  [{lo:7.3f}, {hi:7.3f}]")
        rd = residual_trajectory(d, beta, X)
        rslope = summarise_residual(rd, f"lit tau={tau_m}m")
        results[f"lit_tau{int(tau_m)}"] = {
            "betas": betas, "resid_slope_post": rslope,
            "beta": beta, "X": X, "rd": rd}

    # (ii) Garmin VO2Max-decay secondary curve
    print(f"\n   --- GARMIN VO2Max-decay decond curve (secondary) ---")
    print(f"   NOTE: per-kg, partially re-encodes weight; treat as secondary.")
    betas, resid, beta, X, names = block_bootstrap_betas(d, None, garmin=True)
    for nm in names:
        pt, lo, hi = betas[nm]
        print(f"     {nm:12s}: {pt:8.3f}  [{lo:7.3f}, {hi:7.3f}]")
    rd = residual_trajectory(d, beta, X)
    rslope = summarise_residual(rd, "garmin vo2 decay")
    results["garmin"] = {"betas": betas, "resid_slope_post": rslope,
                         "beta": beta, "X": X, "rd": rd}

    # representative fit for the sensitivities (literature tau=4 mo)
    rep = results["lit_tau4"]
    rep_beta, rep_X, rep_rd = rep["beta"], rep["X"], rep["rd"]
    ac1 = residual_autocorr(rep_rd, lag=1)
    print(f"\n   Residual lag-1 autocorrelation (post-2023): {ac1:.2f} "
          f"(analytic Theil-Sen CI assumes independence; hence the "
          f"block-bootstrap-of-slope headline CI).")

    # --- CI correction across the block grid (reviewer fix 1) ---
    print("\n   --- FIX 1: block bootstrap OF THE RESIDUAL SLOPE, block grid ---")
    ci_grid = {}
    for bl in BLOCK_LEN_GRID:
        lo, hi = block_bootstrap_residual_slope(rep_rd, bl)
        ci_grid[bl] = (lo, hi)
        print(f"     block={bl:2d}d: [{lo:.2f}, {hi:.2f}]")

    # --- leave-one-year-out (reviewer fix 2) ---
    print("\n   --- FIX 2: leave-one-year-out (post-2023 residual slope) ---")
    loyo = {}
    for yr in [2024, 2025, 2026]:
        sl, cl, ch = post2023_slope_analytic(rep_rd, drop_year=yr)
        loyo[yr] = (sl, cl, ch)
        crosses = "CROSSES ZERO" if cl <= 0 <= ch else "excludes zero"
        print(f"     drop {yr}: {sl:.2f} [{cl:.2f}, {ch:.2f}]  {crosses}")
    # per-year residual slopes for the wobble narrative
    per_year = {}
    for yr in [2024, 2025, 2026]:
        dts = pd.to_datetime(rep_rd["date"])
        sub = rep_rd[dts.dt.year == yr]
        t = (pd.to_datetime(sub["date"]) - pd.to_datetime(sub["date"]).min()
             ).dt.days / 365.25
        s2, ic, cl, ch = theilslopes(sub["lc_resid"].values, t.values, 0.95)
        per_year[yr] = s2
    print(f"     per-year residual slopes: "
          f"2024 {per_year[2024]:.2f}, 2025 {per_year[2025]:.2f}, "
          f"2026 {per_year[2026]:.2f}")

    # --- weight coefficient sensitivity (reviewer fix 3) ---
    print("\n   --- FIX 3: weight-coefficient sensitivity ---")
    dts = pd.to_datetime(rep_rd["date"])
    m24 = dts >= POST_2023
    w24 = d.loc[m24.values, "weight_kg"].values
    w_contrib = rep_beta[2] * (w24[-1] - w24[0])
    print(f"     interpolated weight over 2024-2026: {w24[0]:.1f} -> {w24[-1]:.1f} "
          f"kg, removes {w_contrib:.2f} bpm at b_weight={rep_beta[2]:.3f}.")
    weight_sens = {}
    for mult in [1.0, 2.0]:
        beta2 = rep_beta.copy()
        beta2[2] = rep_beta[2] * mult
        rd2 = residual_trajectory(d, beta2, rep_X)
        sl, cl, ch = post2023_slope_analytic(rd2)
        weight_sens[mult] = (rep_beta[2] * mult, sl, cl, ch)
        print(f"     b_weight x{mult:.0f} ({rep_beta[2]*mult:.3f}): "
              f"resid {sl:.2f} [{cl:.2f}, {ch:.2f}]")

    # --- slow / never-plateauing deconditioning sensitivity (reviewer fix 4) ---
    print("\n   --- FIX 4: slow-deconditioning sensitivity (circularity defeater) ---")
    decond_sens = {}
    for label, tau_y in [("plateau_yr2", 1.0), ("plateau_yr3", 2.0),
                         ("plateau_yr4", 4.0)]:
        sh = decond_lit(d["t_cess"].values, tau_y)
        X2, _ = design_matrix(d, sh)
        b2 = fit_robust(X2, d["resting_hr"].values.astype(float))
        rd2 = residual_trajectory(d, b2, X2)
        sl, cl, ch = post2023_slope_analytic(rd2)
        decond_sens[label] = (b2[1], sl, cl, ch)
        print(f"     {label:12s}: decond_amp={b2[1]:.2f} resid {sl:.2f} "
              f"[{cl:.2f}, {ch:.2f}]")
    # linear-forever ramp (never plateaus; amplitude unbounded, adversarial)
    sh = decond_linear_forever(d["t_cess"].values)
    X2, _ = design_matrix(d, sh)
    b2 = fit_robust(X2, d["resting_hr"].values.astype(float), decond_hi=np.inf)
    rd2 = residual_trajectory(d, b2, X2)
    sl, cl, ch = post2023_slope_analytic(rd2)
    lf_rise = b2[1] * (d["t_cess"].max() - 0.0)  # bpm rise implied across span
    decond_sens["linear_forever"] = (b2[1], sl, cl, ch)
    print(f"     linear_forever: decond_amp={b2[1]:.2f} bpm/yr "
          f"(implies {lf_rise:.2f} bpm rise across the post-cess span) "
          f"resid {sl:.2f} [{cl:.2f}, {ch:.2f}]")

    # --- aging bound sensitivity (reviewer fix 5) ---
    print("\n   --- FIX 5: aging-bound sensitivity (0.5 cap OVER-removes aging) ---")
    print(f"     Lit aging bound (resting_hr_aging_magnitude_review.md): "
          f"~0 to ~{AGING_UPPER_BOUND_BPM_YR} bpm/yr upper, central near zero "
          f"or negative. The model's {AGING_CAP_BPM_YR} cap over-removes aging.")
    age_years = d["age_years"].values
    base_resid = rep_rd["lc_resid"].values
    aging_sens = {}
    for new_age in [AGING_CAP_BPM_YR, AGING_UPPER_BOUND_BPM_YR, 0.0]:
        # hold all other coefficients fixed; vary only the aging amount removed.
        # base residual has 0.5*age removed, so add back (0.5 - new_age)*age.
        adj = base_resid + (rep_beta[6] - new_age) * age_years
        rd3 = pd.DataFrame({"date": d["date"].values, "lc_resid": adj})
        sl, cl, ch = post2023_slope_analytic(rd3)
        aging_sens[new_age] = (sl, cl, ch)
        note = ("model cap; OVER-removes" if new_age == AGING_CAP_BPM_YR
                else "lit upper bound" if new_age == AGING_UPPER_BOUND_BPM_YR
                else "aging = 0")
        print(f"     aging removed at {new_age:.1f} ({note}): "
              f"resid {sl:.2f} [{cl:.2f}, {ch:.2f}]")

    # citalopram beta summary
    print("\n   --- Citalopram in-sample RHR beta ---")
    cit = {}
    for key in ["lit_tau3", "lit_tau4", "lit_tau6", "garmin"]:
        pt, lo, hi = results[key]["betas"]["b_dose"]
        ss = pt * 30.0
        ss_lo, ss_hi = lo * 30.0, hi * 30.0
        cit[key] = (pt, ss, ss_lo, ss_hi)
        print(f"     {key:14s}: b_dose={pt:.4f} bpm/mg -> "
              f"{ss:.2f} bpm at 30mg SS [{ss_lo:.2f},{ss_hi:.2f}]  "
              f"[prior {CITALOPRAM_PRIOR_BPM}]")

    # citalopram-era raw RHR slope (drug pushes DOWN, RHR still rises)
    d2 = d[d["date"] >= d.loc[d["dose"] > 0, "date"].min()]
    t = (pd.to_datetime(d2["date"]) - pd.to_datetime(d2["date"]).min()
         ).dt.days / 365.25
    sl, ic, cl, ch = theilslopes(d2["resting_hr"].values, t.values, 0.95)
    cit_era = (sl, cl, ch)
    print(f"   Citalopram-era raw RHR slope (2024-04+): {sl:.2f} bpm/yr "
          f"[{cl:.2f},{ch:.2f}] -- RHR rises WHILE the drug pushes it down.")

    results["_sens"] = {
        "ac1": ac1, "ci_grid": ci_grid, "loyo": loyo, "per_year": per_year,
        "weight_sens": weight_sens, "w_contrib": w_contrib,
        "w24_lo": w24[0], "w24_hi": w24[-1], "decond_sens": decond_sens,
        "aging_sens": aging_sens, "cit": cit, "cit_era": cit_era,
        "rep_betas": results["lit_tau4"]["betas"],
    }
    return results


