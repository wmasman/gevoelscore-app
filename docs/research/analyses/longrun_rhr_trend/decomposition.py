def robust_slope_per_year(dates, values):
    """Theil-Sen slope in bpm/year. Returns (slope, lo, hi) 95%."""
    mask = ~np.isnan(values)
    d = pd.to_datetime(dates[mask])
    v = values[mask]
    if len(v) < 5:
        return np.nan, np.nan, np.nan
    t_years = (d - d.min()).days / 365.25
    slope, intercept, lo, hi = theilslopes(v, t_years, alpha=0.95)
    return slope, lo, hi


def window_slope_raw(df, lo, hi):
    """Raw-daily Theil-Sen slope over a date window."""
    m = (df["date"] >= lo) & (df["date"] <= hi)
    sub = df[m]
    return robust_slope_per_year(
        sub["date"].values.astype("datetime64[ns]"),
        sub["resting_hr"].values.astype(float),
    ) + (int(sub["resting_hr"].notna().sum()),)


def rolling_median_slope(df, lo, hi):
    """28-day centred rolling-median trajectory slope (sidesteps integer-tie
    degeneracy of raw-daily Theil-Sen; reviewer fix 5b)."""
    s = df.set_index("date")["resting_hr"].asfreq("D")
    roll = s.rolling(28, center=True, min_periods=10).median()
    sub = roll.loc[lo:hi].dropna()
    t = (sub.index - sub.index.min()).days / 365.25
    sl, ic, cl, ch = theilslopes(sub.values, t, 0.95)
    return sl, cl, ch, len(sub)


