def build_daily_frame(df, wt, vo):
    """Daily grid over the RHR-observed span with all regressors."""
    d = df[df["resting_hr"].notna()].copy()
    d = d[["date", "resting_hr", "dose_plasma_mg"]].reset_index(drop=True)

    t0 = CESSATION
    # time since cessation in years (clamped at 0 before cessation)
    dt_years = (d["date"] - t0).dt.days / 365.25
    d["t_cess"] = dt_years.clip(lower=0.0)
    d["post_cess"] = (d["date"] >= t0).astype(float)

    # aging: years since start of record
    d["age_years"] = (d["date"] - d["date"].min()).dt.days / 365.25

    # seasonal phase (day-of-year)
    doy = d["date"].dt.dayofyear
    d["sin"] = np.sin(2 * np.pi * doy / 365.25)
    d["cos"] = np.cos(2 * np.pi * doy / 365.25)

    # citalopram plasma (mg)
    d["dose"] = d["dose_plasma_mg"].fillna(0.0)

    # weight interpolated to daily (linear), carry-forward outside the hull
    wser = wt.set_index("date")["weight_kg"]
    grid = pd.Series(index=pd.DatetimeIndex(d["date"]), dtype=float)
    combined = pd.concat([wser, grid[~grid.index.isin(wser.index)]]).sort_index()
    combined = combined.interpolate(method="time", limit_direction="both")
    d["weight_kg"] = combined.reindex(pd.DatetimeIndex(d["date"])).values

    # Garmin VO2Max mapped to RHR-up contribution (secondary decond curve).
    vser = vo.set_index("date")["vo2"]
    vgrid = pd.concat([vser, grid[~grid.index.isin(vser.index)]]).sort_index()
    vgrid = vgrid.interpolate(method="time", limit_direction="both")
    vo2_daily = vgrid.reindex(pd.DatetimeIndex(d["date"])).values
    vo2_peak = np.nanmax(vser.values)
    floss = (vo2_peak - vo2_daily) / vo2_peak
    floss = np.clip(floss, 0.0, None)
    floss = np.where(d["date"].values >= np.datetime64(t0), floss, 0.0)
    d["vo2_floss"] = floss

    return d


def decond_lit(t_cess, tau_years):
    """Unit literature deconditioning curve: exp approach to plateau, 0..1."""
    return 1.0 - np.exp(-t_cess / tau_years)


def decond_linear_forever(t_cess):
    """Never-plateauing linear ramp in years since cessation (0 before).
    This is the deliberately deconditioning-flattering shape: it lets the
    fitness term keep rising through year 4 with no plateau at all."""
    return t_cess


def design_matrix(d, decond_shape):
    """Columns: intercept, decond(unit), weight(centred), dose, sin, cos, age."""
    w_c = d["weight_kg"].values - ATHLETE_BASELINE_KG
    X = np.column_stack([
        np.ones(len(d)),
        decond_shape,
        w_c,
        d["dose"].values,
        d["sin"].values,
        d["cos"].values,
        d["age_years"].values,
    ])
    names = ["intercept", "decond_amp", "b_weight", "b_dose",
             "b_sin", "b_cos", "b_age"]
    return X, names


def fit_robust(X, y, age_cap=AGING_CAP_BPM_YR, decond_hi=LIT_DECOND_BPM_HI):
    """Huber-robust linear fit via least_squares soft_l1 loss.
    Bounds: decond_amp in the literature bpm range; b_age in [0, age_cap]."""
    n_par = X.shape[1]
    lb = np.full(n_par, -np.inf)
    ub = np.full(n_par, np.inf)
    lb[1], ub[1] = 0.0, decond_hi
    lb[6], ub[6] = 0.0, age_cap

    def resid(beta):
        return X @ beta - y

    beta0 = np.zeros(n_par)
    beta0[0] = np.median(y)
    beta0[1] = 6.0
    sol = least_squares(resid, beta0, bounds=(lb, ub),
                        loss="soft_l1", f_scale=2.0, max_nfev=10000)
    return sol.x


def block_bootstrap_betas(d, decond_shape_fn, tau_years=None, garmin=False,
                          linear_forever=False):
    """Block bootstrap over contiguous day-blocks; refit each resample.
    Returns dict name->(point, lo, hi), the residual series, point betas, X."""
    y = d["resting_hr"].values.astype(float)
    if garmin:
        shape = d["vo2_floss"].values
    elif linear_forever:
        shape = decond_linear_forever(d["t_cess"].values)
    else:
        shape = decond_lit(d["t_cess"].values, tau_years)
    # linear-forever ramp is unbounded in amplitude physiologically; allow it
    # to grow so it can TRY to absorb the late rise (adversarial to our claim)
    decond_hi = np.inf if linear_forever else LIT_DECOND_BPM_HI
    X, names = design_matrix(d, shape)

    beta_pt = fit_robust(X, y, decond_hi=decond_hi)
    resid_pt = y - X @ beta_pt

    decond_contrib = X[:, 1] * beta_pt[1]
    print(f"     [decond term contribution span: "
          f"{decond_contrib.min():.2f} to {decond_contrib.max():.2f} bpm]")

    n = len(d)
    starts_all = np.arange(0, n - BLOCK_LEN + 1)
    boot = np.zeros((N_BOOT, len(names)))
    for b in range(N_BOOT):
        idx = []
        while len(idx) < n:
            s = rng.integers(0, len(starts_all))
            idx.extend(range(starts_all[s], starts_all[s] + BLOCK_LEN))
        idx = np.array(idx[:n])
        Xi, yi = X[idx], y[idx]
        try:
            boot[b] = fit_robust(Xi, yi, decond_hi=decond_hi)
        except Exception:
            boot[b] = beta_pt

    out = {}
    for j, nm in enumerate(names):
        lo, hi = np.percentile(boot[:, j], [2.5, 97.5])
        out[nm] = (beta_pt[j], lo, hi)
    return out, resid_pt, beta_pt, X, names


def residual_trajectory(d, beta, X):
    """LC residual = observed - (all modelled drivers except intercept).
    Keeps the intercept so it sits at an interpretable RHR level."""
    contrib = X @ beta
    intercept = beta[0]
    lc_resid = d["resting_hr"].values - (contrib - intercept)
    return pd.DataFrame({"date": d["date"].values, "lc_resid": lc_resid})


def _post_slice(rd, drop_year=None):
    dts = pd.to_datetime(rd["date"])
    m = dts >= POST_2023
    if drop_year is not None:
        m &= dts.dt.year != drop_year
    post = rd[m].reset_index(drop=True)
    tdates = pd.to_datetime(post["date"])
    t = (tdates - tdates.min()).dt.days.values / 365.25
    return post["lc_resid"].values.astype(float), t


def post2023_slope_analytic(rd, drop_year=None):
    """Analytic Theil-Sen slope + CI (NOT autocorrelation-corrected)."""
    y, t = _post_slice(rd, drop_year=drop_year)
    sl, ic, cl, ch = theilslopes(y, t, 0.95)
    return sl, cl, ch


def block_bootstrap_residual_slope(rd, block_len, drop_year=None):
    """PROPER block bootstrap OF THE RESIDUAL SLOPE ITSELF (reviewer fix 1).
    Resample the post-2023 residual daily series in contiguous (t, y) block
    PAIRS (preserving intra-block trend), recompute the Theil-Sen slope per
    resample, take the 2.5/97.5 percentiles. This is what handles the
    residual's lag-1 autocorrelation (~0.94); the old analytic Theil-Sen
    interval assumed near-independence and was ~3x too tight."""
    y, t = _post_slice(rd, drop_year=drop_year)
    n = len(y)
    starts_all = np.arange(0, n - block_len + 1)
    slopes = np.zeros(N_BOOT)
    for b in range(N_BOOT):
        ti, yi = [], []
        while len(yi) < n:
            s = rng.integers(0, len(starts_all))
            blk = range(starts_all[s], starts_all[s] + block_len)
            ti.extend(t[list(blk)])
            yi.extend(y[list(blk)])
        ti = np.array(ti[:n])
        yi = np.array(yi[:n])
        sl, ic, cl, ch = theilslopes(yi, ti, 0.95)
        slopes[b] = sl
    lo, hi = np.percentile(slopes, [2.5, 97.5])
    return lo, hi


def residual_autocorr(rd, lag=1):
    y, _ = _post_slice(rd)
    y = y - np.mean(y)
    return float(np.corrcoef(y[:-lag], y[lag:])[0, 1])


